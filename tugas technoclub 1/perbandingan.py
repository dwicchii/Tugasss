# operator parbandingan
# sama dengan(=)
x = 22
y = 22

if x == y:
    print("sama aja")
else:
    print("ngga sama")

# lebih dari(>)
bola_tennis = 20
bola_basket = 10

if bola_tennis > bola_basket:
    print("bola tennis lebih banyak daripada bola basket")
else:
    print("bola tennis ngga lebih banyak daripada bola basket")

# kurang dari sama dengan(<=)
x = 12
y = 12

if x <= y:
    print("si x kurang dari sama kayak si y")
else:
    print("si x kayaknya sih ngga sama si y")